package com.techelevator.model;

public class updateStatement {
	String city;
	String State;
	String current_preference;
	/**
	 * @return the city
	 */
	public String getCity() {
		return city;
	}
	/**
	 * @param city the city to set
	 */
	public void setCity(String city) {
		this.city = city;
	}
	/**
	 * @return the state
	 */
	public String getState() {
		return State;
	}
	/**
	 * @param state the state to set
	 */
	public void setState(String state) {
		State = state;
	}
	/**
	 * @return the current_prefence
	 */
	public String getCurrent_preference() {
		return current_preference;
	}
	/**
	 * @param current_prefence the current_prefence to set
	 */
	public void setCurrent_preference(String current_preference) {
		this.current_preference = current_preference;
	}
	
}
